#include <stdio.h>
/**
 * main - this is a function for writting lower and upper alpha
 * int c,v - lowercase and capital variables
 * Return: 0
 */
int main(void)
{
	char c, v;
{
	for (c = 'a'; c <= 'z'; c++)
	putchar (c);
}
{
	for (v = 'A'; v <= 'Z'; v++)
	putchar (v);
	putchar ('\n');
}
	return (0);
}
